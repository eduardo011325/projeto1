
#!/usr/bin/python3

num1 = float(input("Digite o primeiro número: "))
num2 = float(input("Digite o segundo número: "))
operação = input("Digite a operação desejada: ")

if operação == "+":
 print("O resultado : ", num1+num2)

elif operação == "-":
 print("O Resultado é: ", num1-num2)

elif operação == "*":
 print("O Resultado é: ", num1*num2)

elif operação == "/":
 print("O Resultado é: ", num1/num2)
